public class Assignment_operator {
    public static void main(String[] args) {
        int a = 15, b = 45;
        a += b;
        System.out.println("The Addition = " + a);
        a -= b;
        System.out.println("The Subtraction = " + a);
        a *= b;
        System.out.println("The Multiplication = " + a);
        a /= b;
        System.out.println("The Divison = " + a);
        a %= b;
        System.out.println("The Modulus = " + a);
    }
}
